void print_pikachu_bar(int current, int total) {
    double progress = (double)current / total;
    int pos = BAR_WIDTH * progress;

    // ьЭ┤ыкиэЛ░ь╜Ш ыЛмыжмыКФ ыкиьЕШ (ьзЭьИШ/эЩАьИШьЧР ыФ░ыЭ╝ ыкиьЦС ы│Аъ▓╜)
    // ыЛмыжмыКФ ыКРыВМьЭД ьЬДэХ┤ эМФ/ыЛдыжм ыкиьЦСьЭД ы░Фъ┐И
    char *zombie = (current % 2 == 0) ? "( >┬┤р╜А`)>" : "( ^┬┤р╜А`)^";

    printf("\r  "); // ьдД ь▓ШьЭМьЬ╝ыбЬ

    // ь▒ДьЫМьзД ы╢Аы╢Д (ы╝И)
    printf("%s", WHITE);
    for (int i = 0; i < BAR_WIDTH; i++) {
        if (i < pos) {
            printf("ЁЯж┤"); 
        } else if (i == pos) {
            // эШДьЮм ьЬДь╣ШьЧР ьвАы╣Д ь╢Ьыае
            printf("%s%s", RESET, zombie);
        } else {
            // эФДыбЬъ╖╕ыаИьКды░Ф ы╣И ъ│╡ъ░Д
            printf("  ");
        }
    }

    // эН╝ьД╝эК╕ ы░П ьГЙьГБ ы│╡ъ╖А
    printf("%s %3d%%", RESET, (int)(progress * 100));
    fflush(stdout);
}